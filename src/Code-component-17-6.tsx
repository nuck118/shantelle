        // Sparkle generation and management
        class SparkleManager {
            constructor(containerId) {
                this.container = document.getElementById(containerId);
                this.sparkles = [];
                this.animationId = null;
                this.init();
            }

            init() {
                this.generateSparkles();
                this.startAnimation();
                window.addEventListener('resize', () => this.generateSparkles());
            }

            generateSparkles() {
                if (!this.container) return;
                
                this.container.innerHTML = '';
                this.sparkles = [];
                
                const sparkleCount = window.innerWidth < 768 ? 8 : 15;
                const sparkleTypes = ['star', 'sparkle', 'crown'];
                const sparkleColors = ['gold', 'white', 'pearl'];
                
                for (let i = 0; i < sparkleCount; i++) {
                    const sparkle = {
                        id: i,
                        x: Math.random() * 100,
                        y: Math.random() * 100,
                        size: Math.random() * 12 + 6,
                        delay: Math.random() * 4,
                        type: sparkleTypes[Math.floor(Math.random() * sparkleTypes.length)],
                        color: sparkleColors[Math.floor(Math.random() * sparkleColors.length)],
                        element: null
                    };
                    
                    sparkle.element = this.createSparkleElement(sparkle);
                    this.container.appendChild(sparkle.element);
                    this.sparkles.push(sparkle);
                }

                // Add floating particles
                const particleCount = window.innerWidth < 768 ? 3 : 6;
                for (let i = 0; i < particleCount; i++) {
                    const particle = this.createParticle(i);
                    this.container.appendChild(particle);
                }
            }

            createSparkleElement(sparkle) {
                const element = document.createElement('div');
                element.className = 'sparkle absolute pointer-events-none';
                element.style.left = sparkle.x + '%';
                element.style.top = sparkle.y + '%';
                element.style.animationDelay = sparkle.delay + 's';
                
                const colorClasses = {
                    gold: 'text-amber-300 drop-shadow-[0_0_6px_rgba(255,215,0,0.8)]',
                    white: 'text-white drop-shadow-[0_0_4px_rgba(255,255,255,0.6)]',
                    pearl: 'text-slate-100 drop-shadow-[0_0_3px_rgba(248,250,252,0.5)]'
                };
                
                let svg = '';
                if (sparkle.type === 'star' || sparkle.type === 'sparkle') {
                    svg = `<svg class="animate-sparkle ${colorClasses[sparkle.color]}" width="${sparkle.size}" height="${sparkle.size}" fill="currentColor" viewBox="0 0 24 24">
                        <path d="M12 0l3.09 6.26L22 9l-6.91 2.74L12 18l-3.09-6.26L2 9l6.91-2.74L12 0z"/>
                    </svg>`;
                } else {
                    svg = `<svg class="animate-sparkle ${colorClasses[sparkle.color]}" width="${sparkle.size}" height="${sparkle.size}" fill="currentColor" viewBox="0 0 24 24">
                        <path d="M12 2l3.09 6.26L22 9l-6.91 2.74L12 18l-3.09-6.26L2 9l6.91-2.74L12 2z"/>
                    </svg>`;
                }
                
                element.innerHTML = svg;
                return element;
            }

            createParticle(index) {
                const particle = document.createElement('div');
                particle.className = 'absolute w-1 h-1 rounded-full bg-gradient-to-r from-amber-300 to-white animate-float pointer-events-none';
                particle.style.left = Math.random() * 100 + '%';
                particle.style.top = Math.random() * 100 + '%';
                particle.style.animationDelay = (index * 1.5) + 's';
                return particle;
            }

            startAnimation() {
                // Regenerate sparkles every 12 seconds
                setInterval(() => {
                    this.generateSparkles();
                }, 12000);
            }
        }

        // Scroll management
        class ScrollManager {
            constructor() {
                this.heroSection = document.getElementById('hero');
                this.invitationSection = document.getElementById('invitation');
                this.currentSection = 'hero';
                this.init();
            }

            init() {
                window.addEventListener('scroll', () => this.handleScroll());
                this.handleScroll(); // Initial call
            }

            handleScroll() {
                const scrollTop = window.pageYOffset;
                const windowHeight = window.innerHeight;
                const docHeight = document.documentElement.scrollHeight;
                
                const totalScrollableHeight = docHeight - windowHeight;
                const scrollProgress = scrollTop / totalScrollableHeight;
                
                // Switch sections at 50% scroll
                if (scrollProgress > 0.5 && this.currentSection === 'hero') {
                    this.showInvitation();
                } else if (scrollProgress <= 0.5 && this.currentSection === 'invitation') {
                    this.showHero();
                }
            }

            showHero() {
                this.heroSection.style.display = 'block';
                this.invitationSection.style.display = 'none';
                this.currentSection = 'hero';
            }

            showInvitation() {
                this.heroSection.style.display = 'none';
                this.invitationSection.style.display = 'block';
                this.currentSection = 'invitation';
                
                // Trigger invitation animations
                this.triggerInvitationAnimations();
            }

            triggerInvitationAnimations() {
                // Add invitation-specific animation classes
                const invitationElements = document.querySelectorAll('.invitation-fade-in');
                invitationElements.forEach((el, index) => {
                    el.style.animation = `fadeInUp 0.8s cubic-bezier(0.22, 1, 0.36, 1) forwards`;
                    el.style.animationDelay = (index * 0.1) + 's';
                });

                const slideElements = document.querySelectorAll('.invitation-slide-line');
                slideElements.forEach((el, index) => {
                    el.style.animation = `slideInLine 1.5s ease-out forwards`;
                    el.style.animationDelay = (index * 0.2) + 's';
                });
            }
        }

        // Initialize everything when DOM is loaded
        document.addEventListener('DOMContentLoaded', function() {
            // Initialize sparkle managers for both sections
            new SparkleManager('sparkles-container');
            new SparkleManager('sparkles-container-2');
            
            // Initialize scroll manager
            new ScrollManager();
            
            // Add smooth scrolling behavior
            document.querySelectorAll('a[href^="#"]').forEach(anchor => {
                anchor.addEventListener('click', function (e) {
                    e.preventDefault();
                    const target = document.querySelector(this.getAttribute('href'));
                    if (target) {
                        target.scrollIntoView({
                            behavior: 'smooth',
                            block: 'start'
                        });
                    }
                });
            });
        });

        // Performance optimization for mobile
        let ticking = false;
        function updateOnScroll() {
            if (!ticking) {
                requestAnimationFrame(function() {
                    // Scroll handling is done in ScrollManager
                    ticking = false;
                });
                ticking = true;
            }
        }

